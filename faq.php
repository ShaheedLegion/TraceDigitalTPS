		<h4>I have purchased an EPUB book, it shows on my iPad/Android, but does not work on my PC!?</h4>
		<p>
		iPads and Android devices have built-in software which can view ePUB, PDF and MOBI files, but for PCs and MACs one would have to download special software to view these files - even PDF files require special software to view on a PC/Mac.<br/>
		To view EPUB files on a PC:
		<ol>
		<li>Open your favourite web browser and navigate to the following <i>URL</i>: <b><a href="http://www.adobe.com/support/digitaleditions/downloads.html">http://www.adobe.com/support/digitaleditions/downloads.html</a></b></li>
		<li>Download the latest ADE version for your particular operating system</li>
		<li>Once the download is complete, run the installer file you downloaded and follow the on-screen prompts.</li>
		<li>Your computer may need to restart during the installation process, if prompted to do so, restart your pc.</li>
		<li>Navigate to your EPUB file, right click on the file and choose "Open with -> Adobe Digital Editions" from the pop up context menu which appears.</li>
		</ol>
		
		You should now be able to view EPUB files on your computer without problems. Please note that <i>ADE is not our software</i> so if you need help with ADE-related issues, then please contact ADOBE for assistance.
		</p>
